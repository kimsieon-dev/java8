package java8;

import java.io.IOException;

public class Day20031701 {
	public static void main(String[] args) throws IOException {
		/*
		 * IOException : 입출력 예외
		 * I -> Input
		 * O -> Output
		 * Exception -> 예외
		 */
		
		System.out.println("키보드를 눌러서 키를 입력해 주세요.");
		int a = System.in.read();
		System.out.println(a);
	}
}
